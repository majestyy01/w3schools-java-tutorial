public class multiplevariables{
    public static void main(String[] args) {
        //example 1
        int xx = 10;
        int yy = 5;
        int zz = 4;
        System.out.println(xx+yy+zz);
        //example 2
        int x=10,y=5,z=4;
        System.out.println(x+y+z);
        //example 3
        int xxx, yyy, zzz;
        xxx = yyy = zzz = 50;
        System.out.println(xxx+yyy+zzz);    
    }
}