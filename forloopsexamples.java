public class forloopsexamples{
    public static void main(String[] args) {
        //example1
        for(int i=0;i<=100;i+=10){
            System.out.println(i);
        }
        //example2
        for(int i = 0;i<=10;i +=2){
            System.out.println("i");
        }
        int sayi = 2;
        for(int i = 1;i<=10;i++){
            System.out.println(sayi + " X " + i + " = "+(sayi * i));
        }

    }
}